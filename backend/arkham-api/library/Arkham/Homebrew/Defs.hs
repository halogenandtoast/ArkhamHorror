{-# LANGUAGE TemplateHaskell #-}

{- | Card-definition tables contributed by homebrew content. Campaigns are
discovered: any @Arkham/Homebrew/<Name>/Defs.hs@ with an 'IsHomebrewDefs'
instance is folded in automatically (imports generated by cards-discover,
instances enumerated by TH) — no edits here when adding a campaign.
-}
module Arkham.Homebrew.Defs where

import Arkham.Card.CardCode
import Arkham.Card.CardDef
import Arkham.Homebrew.DefsBase
import Arkham.Homebrew.DefsEntries ()
import Arkham.Homebrew.TH
import Arkham.Prelude

allHomebrewDefs :: HomebrewDefs
allHomebrewDefs = $(discoverInstances ''IsHomebrewDefs 'homebrewDefs)

defMap :: (HomebrewDefs -> [CardDef]) -> Map CardCode CardDef
defMap slot = mapFromList $ map (toCardCode &&& id) (slot allHomebrewDefs)

locationsMap :: Map CardCode CardDef
locationsMap = defMap hdLocations

enemiesMap :: Map CardCode CardDef
enemiesMap = defMap hdEnemies

treacheriesMap :: Map CardCode CardDef
treacheriesMap = defMap hdTreacheries

playerTreacheriesMap :: Map CardCode CardDef
playerTreacheriesMap = defMap hdPlayerTreacheries

actsMap :: Map CardCode CardDef
actsMap = defMap hdActs

agendasMap :: Map CardCode CardDef
agendasMap = defMap hdAgendas

encounterAssetsMap :: Map CardCode CardDef
encounterAssetsMap = defMap hdEncounterAssets

playerSkillsMap :: Map CardCode CardDef
playerSkillsMap = defMap hdPlayerSkills

storiesMap :: Map CardCode CardDef
storiesMap = defMap hdStories
